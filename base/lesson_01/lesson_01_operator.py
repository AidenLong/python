# -*- coding: utf-8 -*-

# a ** b <=> power(a, b)
print(10 ** 2)
print(10 ** 2.5)

# //:  返回商的整数部分
print(23 // 5)
print(28.7 // 4.4)

# 移位
print(16 << 2)
print(30 << 3)
print(70 >> 1)
print(100 >> 3)

# 与/或/抑或
print(0b1101 & 0b1011)  # 0b1001 = 9
print(0b1101 | 0b1011)  # 0b1111 = 15
print(0b1101 ^ 0b1011)  # 0b0110 = 6
